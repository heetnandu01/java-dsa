// arithmetic operators 

public class Arithmetic {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 15;
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
    }
}
